# Variables de tipo primitivo
nombre = "Carlos"  # cadena (string)
edad = 35  # variable entero (integer)
pi = 3.1416  # variable decimal
llevoGafas = True  # variable booleano
meGustaPython = None  # variable nulo o vacio

print(type(nombre))
print(type(edad))
print(type(pi))
print(type(llevoGafas))
print(type(meGustaPython))
# Tipado dinamico
curso = "Aprende Python"
print(type(curso))
